#ifndef KRONKNET_POOL_H
    #define KRONKNET_POOL_H
    #include <stddef.h>
    #include <sys/socket.h>
    #include <netinet/in.h>
    #include <stdbool.h>

typedef struct kronknet_connection_s knConnection;

///////////////////////////////////////////////////////////////////////////////
/**
 * @struct  knonknet_pool_s
 *
 * @brief   pollfds pool struct, with size and capacity
 */
///////////////////////////////////////////////////////////////////////////////
typedef struct kronknet_pool_s {

    struct pollfd *pollfds;  //!< The array of poolfd
    knConnection **conns;    //!< The connections
    size_t         size;     //!< The capacity of the pool
    size_t         count;    //!< The number of pollfd in the pool

} knPool;
///////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////
/**
 * @brief         Register [fd] insinde [pool], whith [events] events.
 *
 * @param pool    The pool in which [fd] will be register
 * @param fd      The file descriptor to register
 * @param events  The events of [fd]
 * @return        0 on success, -1 otherwise
 */
///////////////////////////////////////////////////////////////////////////////
int knPool_registerFd(knPool *pool, int fd, knConnection *conn, int events);
///////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////
/**
 * @brief         Unregister [fd] from [pool]
 *
 * @param pool    The pool from which [fd] will be unregister
 * @param fd      The file descriptor to unregister
 * @return        0 on success, -1 otherwise
 */
///////////////////////////////////////////////////////////////////////////////
int knPool_unregister(knPool *pool, int fd);
///////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////
/**
 * @brief         Unregister the fd at [index] from [pool]
 *
 * @param pool    The pool from which fd at [index] will be unregister
 * @param index   The index of the file descriptor to unregister
 * @return        0 on success, -1 otherwise
 */
///////////////////////////////////////////////////////////////////////////////
int knPool_unregisterAtIndex(knPool *pool, size_t index);
///////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////
/**
 * @brief         Init the pool
 *
 * @param pool    The pool to init
 * @return        0 on success, -1 otherwise
 */
///////////////////////////////////////////////////////////////////////////////
int knPool_init(knPool *pool);
///////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////
/**
 * @brief         Clear the pool
 *
 * @param pool    The pool to clear
 * @return        Returns nothing
 */
///////////////////////////////////////////////////////////////////////////////
void knPool_clear(knPool *pool);
///////////////////////////////////////////////////////////////////////////////

#endif /* KRONKNET_POOL_H */
